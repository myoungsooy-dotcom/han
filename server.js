// ===== 방 =====
let rooms = {};

// 문제 보내기
function sendQuestion(roomId){
  const room = rooms[roomId];
  const q = questions[Math.floor(Math.random()*questions.length)];

  room.currentQ = q;
  room.answered = false;

  io.to(roomId).emit("question", q);
}

// ===== 소켓 =====
io.on("connection", (socket) => {

  socket.on("createRoom", ()=>{
    const roomId = Math.random().toString(36).substr(2,5);

    rooms[roomId] = {
      players:[socket.id],
      hp:{},
      answered:false
    };

    rooms[roomId].hp[socket.id] = 100;

    socket.join(roomId);
    socket.emit("roomCreated", roomId);
  });

  socket.on("joinRoom",(roomId)=>{
    const room = rooms[roomId];

    if(room && room.players.length < 2){
      room.players.push(socket.id);
      room.hp[socket.id] = 100;

      socket.join(roomId);

      io.to(roomId).emit("startGame");
      sendQuestion(roomId);
    }
  });

  socket.on("answer",({roomId,answerIndex})=>{
    const room = rooms[roomId];
    if(!room || room.answered) return;

    room.answered = true;

    const correct = answerIndex === room.currentQ.c;
    const enemy = room.players.find(p=>p!==socket.id);

    if(correct && enemy){
      room.hp[enemy] -= 20;
    }

    io.to(roomId).emit("result",{
      correct,
      correctAnswer:room.currentQ.c,
      hp:room.hp,
      attacker:socket.id
    });

    if(enemy && room.hp[enemy] <= 0){
      io.to(roomId).emit("gameOver",{winner:socket.id});
      return;
    }

    setTimeout(()=>sendQuestion(roomId),1500);
  });

});

